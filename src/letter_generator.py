def generate_referral_letter(
    nhs_number: str,
    patient_description: str,
    doctor: dict,
    specialty: str,
    imaging: list,
    blood_tests: list,
    triage_result: dict,
    timestamp: str,
) -> bytes:
    """
    Generate an NHS-branded referral letter.

    Args:
        nhs_number:          Patient NHS number.
        patient_description: Free-text patient presentation.
        doctor:              Dict with keys name, role, site.
        specialty:           Referring specialty string.
        imaging:             List of imaging study names.
        blood_tests:         List of blood test names.
        triage_result:       Dict from RAGPipeline (triage_decision, urgency_timeframe, …).
        timestamp:           ISO timestamp string from triage history entry.

    Returns:
        bytes: .docx file content.
    """
    doc = _new_doc()
    _add_letterhead(doc)
    _add_footer(doc)

    triage_lvl  = triage_result.get("triage_decision", "GREEN").upper()
    urgency_txt = triage_result.get("urgency_timeframe", "")
    date_str    = timestamp[:10] if timestamp else datetime.now().strftime("%Y-%m-%d")

    # Date
    _para(doc, datetime.now().strftime("%d %B %Y"), size=11, space_before=8)

    # Urgency badge
    badge_label = _TRIAGE_LABEL.get(triage_lvl, triage_lvl)
    _shaded_para(
        doc,
        f"TRIAGE PRIORITY: {badge_label}  \u2014  {urgency_txt}",
        fill_hex  = _TRIAGE_FILL.get(triage_lvl, "F0F4F5"),
        border_hex= _TRIAGE_BORDER.get(triage_lvl, "AEB7BD"),
        colour    = _TRIAGE_COLOUR.get(triage_lvl, _BLACK),
        size      = 12,
    )

    # Recipient block
    _para(doc, "")
    _para(doc, doctor["name"], bold=True)
    _para(doc, doctor.get("role", ""), space_after=1)
    _para(doc, doctor.get("site", ""), space_after=1)
    _para(doc, "")

    # Re: line
    p_re = doc.add_paragraph()
    p_re.paragraph_format.space_after = Pt(8)
    _run(p_re, "Re: ", bold=True, size=11, colour=_BLUE)
    _run(p_re, f"NHS {nhs_number} \u2014 Referral for {specialty}", size=11)

    # Salutation
    _para(doc, f"Dear {doctor['name']},")
    _para(
        doc,
        "I am writing to refer the above patient for your urgent assessment "
        "following AI-assisted triage via the GP Triage system.",
    )

    # Patient details table
    _section_heading(doc, "Patient Details")
    _kv_table(doc, [
        ("NHS Number",         nhs_number),
        ("Triage Date",        date_str),
        ("Referring System",   "GP Triage AI (Demo)"),
        ("Urgency",            urgency_txt),
        ("AI Confidence",      triage_result.get("confidence", "N/A")),
    ])

    # Presentation
    _section_heading(doc, "Patient Presentation")
    _para(doc, patient_description, space_after=6)

    # Clinical reasoning
    _section_heading(doc, "Clinical Reasoning")
    _para(doc, triage_result.get("clinical_reasoning", ""), space_after=6)

    # Recommended action
    _section_heading(doc, "Recommended Action")
    _para(doc, triage_result.get("recommended_action", ""), space_after=6)

    # Red flags
    red_flags = triage_result.get("red_flags", "None identified")
    _section_heading(doc, "Red Flags")
    flag_colour = _RED if red_flags.lower() != "none identified" else _GREEN
    _para(doc, red_flags, colour=flag_colour, bold=(red_flags.lower() != "none identified"))

    # NICE guideline
    nice = triage_result.get("nice_guideline", "")
    if nice:
        _section_heading(doc, "NICE Guideline Reference")
        _para(doc, nice)

    # Investigations requested
    if imaging or blood_tests:
        _section_heading(doc, "Investigations Requested")
        if imaging:
            p = doc.add_paragraph()
            _run(p, "Imaging:  ", bold=True, size=11)
            _run(p, ", ".join(imaging), size=11)
        if blood_tests:
            p = doc.add_paragraph()
            _run(p, "Blood Tests:  ", bold=True, size=11)
            _run(p, ", ".join(blood_tests), size=11)

    # Sign-off
    _para(doc, "")
    _para(doc, "Yours sincerely,")
    _para(doc, "")
    _para(doc, "GP Triage System (AI-Assisted)", bold=True)
    _para(doc, "Surrey and Sussex Healthcare NHS Trust", size=10, colour=_DARK)
    _para(doc, "GMC Number: [to be completed by authorising clinician]", size=9, colour=_GREY)

    # Disclaimer
    doc.add_paragraph()
    p_disc = doc.add_paragraph()
    pPr = p_disc._p.get_or_add_pPr()
    shd = OxmlElement("w:shd"); shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto"); shd.set(qn("w:fill"), "F0F4F5")
    pPr.append(shd)
    _run(p_disc, _DISCLAIMER, size=9, italic=True, colour=_GREY)

    return _to_bytes(doc)


def generate_admission_letter(nhs_number: str, pathway: dict) -> bytes:
    """Generate an NHS-branded admission confirmation letter."""
    doc = _new_doc()
    _add_letterhead(doc)
    _add_footer(doc)

    s1 = pathway["stages"].get(1,  {}).get("data", {})
    s3 = pathway["stages"].get(3,  {}).get("data", {})
    s5 = pathway["stages"].get(5,  {}).get("data", {})

    _para(doc, datetime.now().strftime("%d %B %Y"), space_before=8)
    _para(doc, "")
    _para(doc, "Re: Admission Confirmation", bold=True, colour=_BLUE, size=12)
    _blue_line(doc)

    _para(doc, f"Dear Patient (NHS: {nhs_number}),")
    _para(
        doc,
        "We are writing to confirm your admission to our hospital. "
        "Please read the following details carefully.",
    )

    _section_heading(doc, "Admission Details")
    _kv_table(doc, [
        ("NHS Number",          nhs_number),
        ("Admission Date",      s5.get("admission_date", "TBC")),
        ("Ward",                s5.get("ward_name", "TBC")),
        ("Bed",                 s5.get("bed_number", "TBC")),
        ("Consultant",          s5.get("admitting_consultant", s3.get("assigned_doctor", "TBC"))),
        ("Admission Type",      s5.get("admission_type", "TBC")),
        ("Hospital",            s5.get("hospital", "East Surrey Hospital")),
    ])

    _section_heading(doc, "What to Bring")
    for item in [
        "Photo ID and NHS number",
        "All current medications (in original packaging)",
        "Glasses, hearing aids, mobility aids if needed",
        "Comfortable clothing and toiletries for your stay",
        "Next-of-kin contact details",
    ]:
        p = doc.add_paragraph(style="List Bullet")
        _run(p, item, size=11)

    _section_heading(doc, "Contact Numbers")
    _kv_table(doc, [
        ("Main Switchboard", "01737 768511"),
        ("Patient Advice",   "01737 768511 ext. 4567"),
        ("NHS 111",          "111"),
    ])

    _para(doc, "")
    _para(doc, "If you have any questions, please do not hesitate to contact us.")
    _para(doc, "")
    _para(doc, "Yours sincerely,")
    _para(doc, "")
    _para(doc, "Admissions Team", bold=True)
    _para(doc, _TRUST_NAME, size=10, colour=_DARK)

    doc.add_paragraph()
    p_disc = doc.add_paragraph()
    _run(p_disc, _DISCLAIMER, size=9, italic=True, colour=_GREY)

    return _to_bytes(doc)


