# ── Email with attachment ──────────────────────────────────────────────────────

def send_letter_email(
    to_email: str,
    subject: str,
    body: str,
    docx_bytes: bytes,
    filename: str,
) -> tuple[bool, str]:
    """
    Send *docx_bytes* as a .docx email attachment via Gmail SMTP.

    Credentials read from SMTP_EMAIL / SMTP_PASSWORD in .env.
    Returns (success, message).
    """
    load_dotenv()
    user     = os.getenv("SMTP_EMAIL")
    password = os.getenv("SMTP_PASSWORD")
    if not user or not password:
        return False, "SMTP credentials not configured in .env."

    msg            = MIMEMultipart()
    msg["From"]    = user
    msg["To"]      = to_email
    msg["Subject"] = subject
    msg.attach(MIMEText(body, "plain"))

    part = MIMEBase(
        "application",
        "vnd.openxmlformats-officedocument.wordprocessingml.document",
    )
    part.set_payload(docx_bytes)
    encoders.encode_base64(part)
    part.add_header("Content-Disposition", f'attachment; filename="{filename}"')
    msg.attach(part)

    try:
        with smtplib.SMTP_SSL("smtp.gmail.com", 465, timeout=10) as srv:
            srv.login(user, password)
            srv.sendmail(user, to_email, msg.as_string())
        return True, f"Sent to {to_email} with attachment {filename}"
    except Exception as exc:
        return False, str(exc)


# ── Internal ───────────────────────────────────────────────────────────────────

def _to_bytes(doc: Document) -> bytes:
    """Save a Document to an in-memory bytes buffer and return the bytes."""
    buf = BytesIO()
    doc.save(buf)
    buf.seek(0)
    return buf.getvalue()
