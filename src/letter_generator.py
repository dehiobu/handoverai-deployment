def _remove_table_borders(table) -> None:
    """Remove all borders from a docx Table using the lxml API."""
    tbl   = table._tbl
    tblPr = tbl.find(qn("w:tblPr"))
    if tblPr is None:
        tblPr = OxmlElement("w:tblPr")
        tbl.insert(0, tblPr)
    existing = tblPr.find(qn("w:tblBorders"))
    if existing is not None:
        tblPr.remove(existing)
    tbl_borders = OxmlElement("w:tblBorders")
    for side in ("top", "left", "bottom", "right", "insideH", "insideV"):
        el = OxmlElement(f"w:{side}")
        el.set(qn("w:val"),   "none")
        el.set(qn("w:sz"),    "0")
        el.set(qn("w:space"), "0")
        el.set(qn("w:color"), "auto")
        tbl_borders.append(el)
    tblPr.append(tbl_borders)


def _add_letterhead(doc) -> None:
    """
    NHS letterhead: 2-column table — trust details left, logo right.
    Followed by an NHS-blue separator line.
    """
    table = doc.add_table(rows=1, cols=2)
    _remove_table_borders(table)

    left_cell  = table.cell(0, 0)
    right_cell = table.cell(0, 1)

    # Trust name
    p1 = left_cell.paragraphs[0]
    p1.paragraph_format.space_after = Pt(2)
    _run(p1, _TRUST_NAME, bold=True, size=13, colour=_BLUE)

    for line in [_TRUST_ADDRESS, _TRUST_TEL, _TRUST_WEB]:
        p = left_cell.add_paragraph()
        p.paragraph_format.space_after = Pt(1)
        _run(p, line, size=9, colour=_DARK)

    # Logo (right-aligned)
    p_logo = right_cell.paragraphs[0]
    p_logo.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    if _LOGO_PATH.exists():
        try:
            run = p_logo.add_run()
            run.add_picture(str(_LOGO_PATH), width=Inches(1.4))
        except Exception:
            _run(p_logo, "NHS", bold=True, size=22, colour=_BLUE)
    else:
        _run(p_logo, "NHS", bold=True, size=22, colour=_BLUE)

    _blue_line(doc)


