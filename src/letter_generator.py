# ── Document helpers ───────────────────────────────────────────────────────────

def _new_doc() -> Document:
    """Create a blank A4 document with NHS default styles."""
    doc = Document()

    # A4 margins
    for section in doc.sections:
        section.top_margin    = Cm(2.0)
        section.bottom_margin = Cm(2.0)
        section.left_margin   = Cm(2.54)
        section.right_margin  = Cm(2.54)

    # Default paragraph font
    style = doc.styles["Normal"]
    style.font.name = "Arial"
    style.font.size = Pt(11)
    style.font.color.rgb = _BLACK

    return doc


def _run(para, text: str, bold=False, italic=False, size: int = 11,
         colour: RGBColor = None) -> None:
    """Append a formatted run to *para*."""
    run = para.add_run(text)
    run.font.name  = "Arial"
    run.font.size  = Pt(size)
    run.font.bold  = bold
    run.font.italic = italic
    if colour:
        run.font.color.rgb = colour


def _para(doc, text: str = "", bold=False, size: int = 11,
          colour: RGBColor = None, align=WD_ALIGN_PARAGRAPH.LEFT,
          space_before: int = 0, space_after: int = 4) -> None:
    """Add a paragraph with a single run to *doc*."""
    p = doc.add_paragraph()
    p.alignment = align
    p.paragraph_format.space_before = Pt(space_before)
    p.paragraph_format.space_after  = Pt(space_after)
    if text:
        _run(p, text, bold=bold, size=size, colour=colour)
    return p


def _blue_line(doc) -> None:
    """Insert a 2pt NHS-blue horizontal rule."""
    p   = doc.add_paragraph()
    pPr = p._p.get_or_add_pPr()
    bdr = OxmlElement("w:pBdr")
    bot = OxmlElement("w:bottom")
    bot.set(qn("w:val"),   "single")
    bot.set(qn("w:sz"),    "12")      # 1.5pt
    bot.set(qn("w:space"), "1")
    bot.set(qn("w:color"), "005EB8")
    bdr.append(bot)
    pPr.append(bdr)
    p.paragraph_format.space_after  = Pt(8)
    p.paragraph_format.space_before = Pt(0)


def _shaded_para(doc, text: str, fill_hex: str, border_hex: str,
                 colour: RGBColor = None, bold=True, size=12) -> None:
    """Paragraph with coloured background — used for urgency badge."""
    p   = doc.add_paragraph()
    pPr = p._p.get_or_add_pPr()

    # Background fill
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"),   "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"),  fill_hex)
    pPr.append(shd)

    # Left border bar
    bdr = OxmlElement("w:pBdr")
    left = OxmlElement("w:left")
    left.set(qn("w:val"),   "single")
    left.set(qn("w:sz"),    "24")
    left.set(qn("w:space"), "4")
    left.set(qn("w:color"), border_hex)
    bdr.append(left)
    pPr.append(bdr)

    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after  = Pt(6)
    p.paragraph_format.left_indent  = Cm(0.3)
    _run(p, text, bold=bold, size=size, colour=colour or _BLACK)


def _section_heading(doc, text: str) -> None:
    """NHS-blue bold section heading with underline rule."""
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(10)
    p.paragraph_format.space_after  = Pt(2)
    _run(p, text.upper(), bold=True, size=11, colour=_BLUE)

    # Thin underline via paragraph bottom border
    pPr = p._p.get_or_add_pPr()
    bdr = OxmlElement("w:pBdr")
    bot = OxmlElement("w:bottom")
    bot.set(qn("w:val"),   "single")
    bot.set(qn("w:sz"),    "4")
    bot.set(qn("w:space"), "1")
    bot.set(qn("w:color"), "005EB8")
    bdr.append(bot)
    pPr.append(bdr)


def _kv_table(doc, rows: list[tuple[str, str]]) -> None:
    """Compact 2-column key-value table (no outer borders, light grey shading on keys)."""
    table = doc.add_table(rows=len(rows), cols=2)
    table.style = "Table Grid"
    for i, (key, val) in enumerate(rows):
        cells = table.rows[i].cells

        # Key cell — light blue shading
        kPr = cells[0]._tc.get_or_add_tcPr()
        shd = OxmlElement("w:shd")
        shd.set(qn("w:val"),   "clear")
        shd.set(qn("w:color"), "auto")
        shd.set(qn("w:fill"),  "E8EEF8")
        kPr.append(shd)

        key_para = cells[0].paragraphs[0]
        _run(key_para, key, bold=True, size=10, colour=_DARK)
        cells[0].width = Cm(4.5)

        val_para = cells[1].paragraphs[0]
        _run(val_para, str(val or "—"), size=10)

    doc.add_paragraph().paragraph_format.space_after = Pt(4)


def _add_footer(doc, page_label: str = "") -> None:
    """NHS footer: page number left, trust tagline centre."""
    section = doc.sections[0]
    footer  = section.footer
    # Clear default content
    for p in footer.paragraphs:
        for run in p.runs:
            run.text = ""

    para = footer.paragraphs[0]
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Page number field
    fld_begin  = OxmlElement("w:fldChar"); fld_begin.set(qn("w:fldCharType"),  "begin")
    instr      = OxmlElement("w:instrText"); instr.text = " PAGE "
    fld_sep    = OxmlElement("w:fldChar"); fld_sep.set(qn("w:fldCharType"),    "separate")
    fld_end    = OxmlElement("w:fldChar"); fld_end.set(qn("w:fldCharType"),    "end")
    num_run    = para.add_run()
    num_run._r.append(fld_begin); num_run._r.append(instr)
    num_run._r.append(fld_sep);   num_run._r.append(fld_end)
    num_run.font.size = Pt(9); num_run.font.color.rgb = _GREY

    sep_run = para.add_run("  |  " + _FOOTER_TEXT)
    sep_run.font.size = Pt(9); sep_run.font.color.rgb = _BLUE


